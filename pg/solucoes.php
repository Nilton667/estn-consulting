                <p>A ESTN CONSULTING desenvolve produtos ou serviços específicos para resolver os problemas sociais.</p>
                <h5><b>Você sabe do que se trata?</b></h5>
                <p>Um empreendimento social é caracterizado como uma iniciativa que é ao mesmo tempo lucrativa e social. Em outras palavras, todo empreendimento que tem como foco principal a resolução (ou minimização) de problemas que afetam a comunidade é um negócio de impacto social.</p>  
                <p>O faturamento desse tipo de empresa é dividido em ações que visam melhorar efetivamente a vida das pessoas. E essas melhorias podem ser buscadas em diferentes áreas, como educação, meio ambiente, segurança, saúde, alimentação, acessibilidade, inclusão social, dentre tantos outros exemplos!</p>
                <h5>O que levaria uma empresa a abrir mão de seu lucro total para buscar soluções para problemas sociais?</h5>
                <p>A resposta é simples: pelo propósito.</p>
                <p>Empreendedores que se dedicam a causas sociais redefinem a ideia de sucesso nos negócios ao buscar o benefício ao próximo acima do retorno financeiro. São pessoas que possuem como característica principal a empatia. Essa parece ser uma tendência crescente no mercado.</p> 
                <h5>Benefícios de transformar o seu negócio em um empreendimento social:</h5>
                <ul>
                    <li><p>É algo muito positivo para a imagem da empresa;</p></li>
                    <li><p>Pode ser um grande diferencial competitivo;</p></li>
                    <li><p>Torna possível a otimização de práticas produtivas de acordo com um novo propósito;</p></li>
                    <li><p>Ajuda a atrair investidores;</p></li>
                    <li><p>Ajuda a atrair e atender as demandas de consumidores cada vez mais conscientes;</p></li>
                    <li><p>É uma maneira de se posicionar enquanto negócio inovador;</p></li>
                    <li><p>Demonstra que a empresa está engajada em um movimento de mudança;</p></li>
                </ul>
                <h5>Precisa de ajuda para mudar o posicionamento da sua empresa?</h5>
