$(document).ready(function(){
  function typewriter(element, time) {
    
    let letters = element.innerHTML.split('');
    element.innerHTML = ''; 
    
    letters.forEach(( letter, index, letters ) => {
      setTimeout(() => element.innerHTML += letter, time * index);
    });
  }

  if(document.querySelector('.typewriter')){
    const text = document.querySelector('.typewriter');
    typewriter(text, 30);
  }
});

